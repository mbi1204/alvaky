package com.sinergitec.calendar.model;

public class OpOSDocs {

	private String cCveCia;
	private Integer iOServID;
	private Integer iPartida;
	private String cDescripcion;
	private String cNombre;
	private byte bImagen;
	private String dtCreado;
	private String dtModificado;
	private String cUsuario;
	
	public String getcCveCia() {
		return cCveCia;
	}
	public void setcCveCia(String cCveCia) {
		this.cCveCia = cCveCia;
	}
	public Integer getiOServID() {
		return iOServID;
	}
	public void setiOServID(Integer iOServID) {
		this.iOServID = iOServID;
	}
	public Integer getiPartida() {
		return iPartida;
	}
	public void setiPartida(Integer iPartida) {
		this.iPartida = iPartida;
	}
	public String getcDescripcion() {
		return cDescripcion;
	}
	public void setcDescripcion(String cDescripcion) {
		this.cDescripcion = cDescripcion;
	}
	public String getcNombre() {
		return cNombre;
	}
	public void setcNombre(String cNombre) {
		this.cNombre = cNombre;
	}
	public byte getbImagen() {
		return bImagen;
	}
	public void setbImagen(byte bImagen) {
		this.bImagen = bImagen;
	}
	public String getDtCreado() {
		return dtCreado;
	}
	public void setDtCreado(String dtCreado) {
		this.dtCreado = dtCreado;
	}
	public String getDtModificado() {
		return dtModificado;
	}
	public void setDtModificado(String dtModificado) {
		this.dtModificado = dtModificado;
	}
	public String getcUsuario() {
		return cUsuario;
	}
	public void setcUsuario(String cUsuario) {
		this.cUsuario = cUsuario;
	}
}
